package me.bergenfly.newmod.core.util;

public class IntUtil {
    //Written by Jonas K from StackOverflow (https://stackoverflow.com/users/26609/jonas-k and https://stackoverflow.com/a/237204)
    public static boolean isInteger(String str) {
        if (str == null) {
            return false;
        }
        int length = str.length();
        if (length == 0) {
            return false;
        }
        int i = 0;
        if (str.charAt(0) == '-') {
            if (length == 1) {
                return false;
            }
            i = 1;
        }
        for (; i < length; i++) {
            char c = str.charAt(i);
            if (c < '0' || c > '9') {
                return false;
            }
        }
        return true;
    }
}
